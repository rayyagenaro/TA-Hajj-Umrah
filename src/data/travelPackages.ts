const parsePriceToNumber = (price: string) => Number(price.replace(/[^\d]/g, ""));
const normalizeDetail = (value: string | undefined, fallback = "Belum dicantumkan di sumber") => {
  if (!value) return fallback;
  const cleaned = value.trim();
  if (!cleaned || cleaned === "-" || cleaned.toUpperCase().includes("#N/A")) return fallback;
  return cleaned;
};

const sanitizePackage = (pkg: TravelPackage): TravelPackage => ({
  ...pkg,
  url: clean(pkg.url),
  transport: normalizeDetail(pkg.transport),
  accommodation: normalizeDetail(pkg.accommodation),
});

const inferBudgetBracket = (price: string): "low" | "mid" | "high" => {
  const value = parsePriceToNumber(price);
  if (value <= 30000000) return "low";
  if (value <= 40000000) return "mid";
  return "high";
};

const filterAvailable = (pkg: TravelPackage) =>
  pkg.availablePax === undefined || pkg.availablePax > 0;

const enrichAndSortPackages = (packages: TravelPackage[]): TravelPackage[] =>
  packages
    .filter(filterAvailable)
    .map(sanitizePackage)
    .map((pkg) => ({
      ...pkg,
      budgetBracket: pkg.budgetBracket ?? inferBudgetBracket(pkg.price),
    }))
    .sort((a, b) => parsePriceToNumber(a.price) - parsePriceToNumber(b.price));

